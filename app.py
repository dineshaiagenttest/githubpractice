def addition(a, b,c):
    return a + b + c

print(addition(5, 3))

def subtraction(a, b, d):
    return a - b - d

def multiplication(a, b):
    return a * b    

print(subtraction(10, 4))
print(multiplication(6, 7))
print(multiplication(6, 7))

def division(a, b):
    if b != 0:
        return a / b
    else:
        return "Error: Division by zero is not allowed."    
